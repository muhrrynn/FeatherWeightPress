<h1>2008 Work</h1>
<b>The Mistake Series</b><BR/><br/>


When a facade breaks down, a new or true reality is revealed.  Sometimes this new reality is mind altering.  Sometimes it creates strife and change.  But what happens when the degradation reveals noting, or reveals nothing new?  Can it still matter? Does it become humorous or is the break down the true revelation? <BR/><br/>


The Mistake Series and most of the work produced in 2008, is a lighthearted approach to my disillusion with modern life.  Using humor to neutralize an uncomfortable situation.  I question the value of objects, symbols and language from our every day lives; isolating and forcing them to not only stand alone but show their weaknesses.  Each object is, in one way or another, deteriorating and revealing it's flaws, structure and suggesting ideas greater than itself. <BR/><br/>

